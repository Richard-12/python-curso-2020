#Tuplas con funciones
#Convertir una tupla en una lista

a=(3,4,57,2,1)
lista=list(a)
print(lista)

#Para convertir listas a tuplas

a=[3,4,57,2,1]
tupla=tuple(a)
print(tupla)

#obtener la longitud de una tupla

a=(3,4,57,2,1)
print(len(a))

#obtener el máximo valor de la tupla

a=(3,4,57,2,1)
print(max(a))

#obtención del mínimo de la tupla
a=(3,4,57,2,1)
print(min(a))
